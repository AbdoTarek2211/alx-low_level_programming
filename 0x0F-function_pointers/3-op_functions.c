#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - add
 * @a: input
 * @b: input
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract
 * @a: input
 * @b: input
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: input
 * @b: input
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: input
 * @b: input
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus
 * @a: input
 * @b: input
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
